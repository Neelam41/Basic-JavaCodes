
//Program to equal operator and not equal operators
package loops;

public class loop3 {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		while(a!=20) {
			a++;
			}
		
		a++;
		
		if(a==b)
		{
			System.out.println("a = b"+"\n"+a+"="+b);
		}
		else {
			System.out.println("a! = b"+"\n"+a+"!="+b);
		}

	}
}
