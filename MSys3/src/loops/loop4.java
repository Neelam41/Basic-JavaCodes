
// Write a program to print the odd and even numbers.

package loops;

public class loop4 {

	public static void main(String[] args) {
		int a=0;
		while(a<=20) {
			
			if(a%2==0) {
				System.out.println("even: "+a);
				
			}
			else
				System.out.println("odd: "+ a);
			
		 a++;	
		}
		
		

	}

}
