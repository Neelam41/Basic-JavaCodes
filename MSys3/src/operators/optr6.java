
// Program for relational operators (<,<==, >, >==)
package operators;

public class optr6 {

	
	public static void main(String[] args) {
		 int a = 10, b = 12;

		 
		    // == operator
		    System.out.println(a == b); 

		    // != operator
		    System.out.println(a != b); 

		    // > operator
		    System.out.println(a > b); 

		    // < operator
		    System.out.println(a < b);  

		    // >= operator
		    System.out.println(a >= b); 

		    // <= operator
		    System.out.println(a <= b);  
		
	}
}
